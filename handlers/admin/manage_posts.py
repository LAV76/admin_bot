        # –ü–æ–ª—É—á–∞–µ–º ID –ø–æ—Å—Ç–∞ –∏–∑ —Å–æ—Å—Ç–æ—è–Ω–∏—è FSM
        state_data = await state.get_data()
        post_id = state_data.get("post_id")
        
        if not post_id:
            # –ï—Å–ª–∏ ID –ø–æ—Å—Ç–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏, –ø—ã—Ç–∞–µ–º—Å—è –∏–∑–≤–ª–µ—á—å –µ–≥–æ –∏–∑ —Å–æ–æ–±—â–µ–Ω–∏—è
            import re
            if callback.message and callback.message.text:
                matches = re.findall(r"ID (\d+)", callback.message.text)
                if matches:
                    post_id = int(matches[0])
    
